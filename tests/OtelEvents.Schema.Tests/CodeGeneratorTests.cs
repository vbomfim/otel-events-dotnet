    // Helper: Roslyn metadata references
    // ═══════════════════════════════════════════════════════════════

    private static MetadataReference[] GetCoreMetadataReferences()
    {
        var trustedAssemblies = ((string)AppContext.GetData("TRUSTED_PLATFORM_ASSEMBLIES")!)
            .Split(Path.PathSeparator);

        var neededAssemblies = new[]
        {
            "System.Runtime",
            "System.Collections",
            "System.Diagnostics.DiagnosticSource",
            "Microsoft.Extensions.Logging.Abstractions",
            "netstandard",
            "System.Private.CoreLib"
        };

        return trustedAssemblies
            .Where(path => neededAssemblies.Any(name =>
                Path.GetFileNameWithoutExtension(path).Equals(name, StringComparison.OrdinalIgnoreCase)))
            .Select(path => MetadataReference.CreateFromFile(path))
            .ToArray<MetadataReference>();
    }
