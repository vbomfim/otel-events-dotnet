    [Fact]
    public void Generate_FullEvent_ProducesCompilableCode()
    {
        var doc = CreateSchemaWithEvent(new EventDefinition
        {
            Name = "order.placed",
            Id = 1001,
            Severity = Severity.Info,
            Message = "Order {orderId} placed",
            Fields =
            [
                new FieldDefinition { Name = "orderId", Type = FieldType.String, Required = true },
                new FieldDefinition { Name = "amount", Type = FieldType.Double, Required = true }
            ],
            Metrics =
            [
                new MetricDefinition { Name = "order.count", Type = MetricType.Counter },
                new MetricDefinition { Name = "order.amount", Type = MetricType.Histogram }
            ]
        });

        var files = _generator.GenerateFromSchema(doc);
        var content = files.First(f => f.FileName.Contains("Events")).Content;

        var syntaxTree = CSharpSyntaxTree.ParseText(content);
        var references = GetCoreMetadataReferences();

        var compilation = CSharpCompilation.Create(
            "TestCompilation",
            [syntaxTree],
            references,
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

        var diagnostics = compilation.GetDiagnostics()
            .Where(d => d.Severity == DiagnosticSeverity.Error)
            .ToList();

        // Partial methods with [LoggerMessage] can't fully compile without the source generator,
        // but all other code should have no errors beyond the partial method body.
        // Filter out CS8795 (partial method must have implementation) which is expected.
        var realErrors = diagnostics
            .Where(d => d.Id != "CS8795") // Partial method must have an implementation part
            .ToList();

        Assert.Empty(realErrors);
    }

